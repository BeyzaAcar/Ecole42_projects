void	ft_ultimate_div_mod(int *a, int *b)
{
	int	bolum;
	int	kalan;

	if ((*b) != 0)
	{
		kalan = (*a) % (*b);
		bolum = (*a) / (*b);
		*a = bolum;
		*b = kalan;
	}
}
